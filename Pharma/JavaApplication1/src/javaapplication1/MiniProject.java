    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        boolean prixTrue=false;
        boolean QuantiteTrue=false;
        boolean nomTrue=false;
        boolean refTrue=false;

        String messageprix="Le prix entré n'est pas valide.";
        String messageNom="Le nom entré n'est pas valide";
        String messageQuantite="Le Quantite entré n'est pas valide.";
        String messageReferance="Le réferance entre n'est pas valide.";
        try{
            Integer.parseInt(prix.getText());
            prixTrue=true;
            prixl.setText("");

        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null,messageprix,"Désolé, tu n'as pas rempli les champs correctement"
                ,JOptionPane.ERROR_MESSAGE);
            prixTrue=false;
            prixl.setText(messageprix);

        }

        try{
            Integer.parseInt(Quantite.getText());
            QuantiteTrue=true;
            ql.setText("");

        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null,messageQuantite,"Désolé, tu n'as pas rempli les champs correctement"
                ,JOptionPane.ERROR_MESSAGE);
            QuantiteTrue=false;
            ql.setText(messageQuantite);

        }

        try{
            Integer.parseInt(ref.getText());
            refTrue=true;
            lr.setText("");

        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null,messageReferance,"Désolé, tu n'as pas rempli les champs correctement"
                ,JOptionPane.ERROR_MESSAGE);
            refTrue=false;
            lr.setText(messageReferance);

        }
        if(Desc.getText() != ""){
            nomTrue=true;
            noml.setText("");

        }else{
            nomTrue=false;
            noml.setText(messageNom);

            JOptionPane.showMessageDialog(null,messageNom,"Désolé, tu n'as pas rempli les champs correctement"
                ,JOptionPane.ERROR_MESSAGE);
        }

        if((QuantiteTrue )&&(prixTrue)&&( nomTrue)&&(refTrue)){
            int  prixs=   Integer.parseInt(prix.getText());
            int quant= Integer.parseInt(Quantite.getText());
            String refe= ref.getText();
            String dsec=Desc.getText();

            String msg=   c.AjouterP(dsec,prixs,quant,refe,newPath,(String)jComboBox1.getSelectedItem());
            JOptionPane.showMessageDialog(null,msg,"Titre",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_AddActionPerformed

