    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        //filter the files
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.Images", "jpg", "gif", "png");

        file.addChoosableFileFilter(filter);
        int result = file.showSaveDialog(null);
        //if the user click on save in Jfilechooser
        if(result == JFileChooser.APPROVE_OPTION){
            File selectedFile = file.getSelectedFile();
            if(filter.accept(selectedFile)){

                path = selectedFile.getAbsolutePath();
                File save   =new SaveImage().save(path,selectedFile.getName());
                newPath=save.getPath();
                image.setIcon(new ImageIcon(newPath));
                image.repaint();
                image.revalidate();
            }else{
                JOptionPane.showMessageDialog(null," le fichier selectionné n'est pas une image  valide","Désolé, tu n'as pas rempli les champs correctement"
                    ,JOptionPane.ERROR_MESSAGE);
            }

        }
        //if the user click on save in Jfilechooser

        else if(result == JFileChooser.CANCEL_OPTION){
            System.out.println("No File Select");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

